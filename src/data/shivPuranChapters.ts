// Helper function to get chapters by Khand
export const getChaptersByKhand = (khand: string): ShivPuranChapter[] => {
  return shivPuranChapters.filter(chapter => chapter.khand === khand || chapter.khandHindi === khand);
};

// Get all unique Khands
export const getAllKhands = (): { name: string; nameHindi: string; chapterCount: number }[] => {
  const khandMap = new Map<string, { nameHindi: string; count: number }>();
  
  shivPuranChapters.forEach(chapter => {
    if (khandMap.has(chapter.khand)) {
      const existing = khandMap.get(chapter.khand)!;
      existing.count++;
    } else {
      khandMap.set(chapter.khand, { nameHindi: chapter.khandHindi, count: 1 });
    }
  });
  
  return Array.from(khandMap.entries()).map(([name, data]) => ({
    name,
    nameHindi: data.nameHindi,
    chapterCount: data.count
  }));
};

// Get total chapter count
export const getTotalChapterCount = (): number => shivPuranChapters.length;
